#!/usr/bin/env bash
read n
d=$((5-$n))
x=16
i=0
s="_________________________________________________1__________________________________________________"
s2="____________________________________________________________________________________________________"
a=($s)
a2=(0 2 4 8 16)
print_lines(){
    i=0
    while [ "$i" -lt "$x" ] ; do
        a+=($s)
        i=$(($i+1))
    done
    s=`sed 's/.1./1_1/g' <<< $s`
    a+=($s)
    i=1
    while [ "$i" -lt "$x" ]; do
        s=`sed -r 's/.1([^1]+)1./1_\1_1/g' <<< $s`
        a+=($s)
        i=$(($i+1))
    done
    x=$(($x/2))
    n=$(($n-1))
}
while [ "$n" -gt 0 ]; do
    print_lines "$n"
done
while [ "$d" -gt 0 ]; do
    for z in $(seq ${a2[$d]}); do
        echo $s2
    done
    d=$(($d-1))
done
echo $s2
i=$((${#a[@]}-1))
while [ "$i" -gt 0 ];do
    echo ${a[$i]}
    i=$(($i-1))
done